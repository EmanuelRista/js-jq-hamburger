$('.fa-bars').click(function(){//se clicchi sulla fa-bars (hamburger) allora si attiva la funzione:
  $('.hamburger-menu').show();//mostra il il menu hamburger
})
$('.close').click(function(){//se flicchi sulla fa-times(X, chiudi) allora si attiva la funzione:
  $('.hamburger-menu').hide();//nascondi il menu hamburger
})
